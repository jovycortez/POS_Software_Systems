    private void btn_loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_loginActionPerformed
        // TODO add your handling code here:
        
        String sql = "select * from emp_login where empID=? and password=?";
        try{
            pst = dbconn.prepareStatement(sql);
            pst.setString(1,txt_empID.getText());
            pst.setString(2,txt_password.getText());
            
            rs = pst.executeQuery();
            if(rs.next()){
                JOptionPane.showMessageDialog(null,"Employee ID and Password is correct!");
            View_Manager mgrView = new View_Manager();
            mgrView.setVisible(true);
            setVisible(false);
            }else{
                JOptionPane.showMessageDialog(null,"Employee ID and Password is not correct!");
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        
            
       
    }//GEN-LAST:event_btn_loginActionPerformed

